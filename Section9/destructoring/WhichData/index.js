//! khi nào dùng Arr and Sets
/*
   - arr : 
          + sử dụng khi bạn cần các giá trị danh sách theo thứ tự (có thể chứa các giá trị trùng lặp)
          + sử dụng khi bạn cần thao tác dữ liệu

   - Sets : 
          + Sử dụng khi làm việc với gia trị duy nhất
          + sử dụng khi hiệu suất cao là thực sự quan trọng
          + sử dụng để loại bỏ các bản sao từ mảng
*/

//! khi nào dùng Object and Maps

/*
    - Object : 
              + Khóa/giá trị "truyền thống" hơn cửa hàng (đối tượng "bị lạm dụng")
              + Viết và truy cập dễ dàng hơn các values với  và []
              + Sử dụng khi bạn cần bao gồm các Func (Method)
              +  Sử dụng khi làm việc với JSON (có thể chuyển đổi thành Map)

    - Maps :  + hiệu suất tốt hơn
              + keys có thể là bất kì kiểu dữ liệu nào
              + dễ lặp lại
              + Dễ dàng tính toán kích thước
              + Sử dụng khi 
              + Sử dụng khi bạn chỉ cần Key to Values
              + sử dụng khi bạn cần các Keys không phải là chuỗi
*/
